FunctionalState getPowerOut_State (CommPortNumber_Type channel)
{
  FunctionalState state;
  switch( channel)
  {
   case Rs485_PortA : 
    {
     if(OUT_CH3_ENpin)
     {state = ENABLE;}
     else
     {state = DISABLE;}
     break;
    }
   case Rs485_PortB:
    {
     if(OUT_CH2_ENpin)
     {state = ENABLE;}
     else
     {state = DISABLE;}
     break;
    }
   case Rs485_PortC:
    {
     if(OUT_CH1_ENpin)
     {state = ENABLE;}
     else
     {state = DISABLE;}
     break;
    }
    case Rs485_ALL:
    {
     if((OUT_CH1_ENpin)||(OUT_CH2_ENpin)||(OUT_CH3_ENpin))
     {state = ENABLE;}
     else
     {state = DISABLE;}
     break;
    } 
   default:
   {
     state = DISABLE;
     break;
   }
  }
  return state;
}
