void PowerOut_Cmd (CommPortNumber_Type channel, FunctionalState NewState)
{ 
  if(Rs485_PortA & channel)
  {
    if(ENABLE == NewState)
    {set_OUT_CH3_ENpin; }
    else
    {clr_OUT_CH3_ENpin;}  
  }
  if(Rs485_PortB & channel)
  {
    if(ENABLE == NewState)
    {set_OUT_CH2_ENpin; }
    else
    {clr_OUT_CH2_ENpin;}  
  }
  if(Rs485_PortC & channel)
  {
    if(ENABLE == NewState)
    {set_OUT_CH1_ENpin; }
    else
    {clr_OUT_CH1_ENpin;}  
  }
}
